const savedLang = localStorage.getItem('innermeter_lang');
const browserLang = navigator.language.split('-')[0];
const supportedCodes = LANGUAGES.map(l => l.code);
const defaultLang = savedLang && supportedCodes.includes(savedLang)
  ? savedLang
  : supportedCodes.includes(browserLang) ? browserLang : 'ko';

i18n.use(initReactI18next).init({
  resources,
  lng: defaultLang,
  fallbackLng: 'ko',
  interpolation: { escapeValue: false },
});

export function setLanguage(code: string) {
  i18n.changeLanguage(code);
  localStorage.setItem('innermeter_lang', code);
  const lang = LANGUAGES.find(l => l.code === code);
  document.documentElement.dir = lang?.rtl ? 'rtl' : 'ltr';
  document.documentElement.lang = code;
}

